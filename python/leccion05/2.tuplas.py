# una tupla es de tipo inmutable (no modificable)

# definir una tupla
frutas = ('Naranja', 'Plátano', 'Guayaba')
print(frutas)

# largo de una tupla
print(len(frutas))

# acceder a un elemento
print(frutas[0])

# navegación inversa
print(frutas[-1])

# acceder a un rango de valores
print(frutas[0:2]) # sin incluir el último índice

# recorrer elementos
for fruta in frutas:
    print(fruta, end=' ')

