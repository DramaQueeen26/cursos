#Operadores de asignacion

#Operador de aignacion =

miVariable = 10

#Operador de reasignacion += o -=

miVariable = 10 + 1
miVariable += 1
print(miVariable)

miVariable -= 2
print(miVariable)

#Se puede hacer con cualquier operador aritmetico

miVariable *= 2
print(miVariable)

miVariable /= 2
print(miVariable)