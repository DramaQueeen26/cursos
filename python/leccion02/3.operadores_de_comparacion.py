a = 4
b = 2

resultado = (a == b) #Si a es igual a b

print("Resultado == : " + str(resultado))

resultado = (a != b) #Si a es diferente de b

print("Resultado != : " + str(resultado))

resultado = (a > b) #Es mayor

print("Resultado > : " + str(resultado))

resultado = (a >= b) #Si es mayor o igual

print("Resultado >= : " + str(resultado))

resultado = (a < b) #Si es menor

print("Resultado < : " + str(resultado))

resultado = (a <= b) #Si es menor o igual

print("Resultado <= : " + str(resultado))






